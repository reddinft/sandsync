def create_title_card(text, subtitle="", output_path="title.png"):
    """Create a title card with text overlay"""
    img = Image.new('RGB', (VIDEO_WIDTH, VIDEO_HEIGHT), color=(15, 23, 42))  # Dark blue
    draw = ImageDraw.Draw(img)
    
    try:
        title_font = ImageFont.truetype("/System/Library/Fonts/SF-Pro-Display-Semibold.otf", 120)
        subtitle_font = ImageFont.truetype("/System/Library/Fonts/SF-Pro-Text-Regular.otf", 48)
    except:
        title_font = ImageFont.load_default()
        subtitle_font = ImageFont.load_default()
    
    # Draw title
    title_bbox = draw.textbbox((0, 0), text, font=title_font)
    title_width = title_bbox[2] - title_bbox[0]
    title_x = (VIDEO_WIDTH - title_width) // 2
    title_y = (VIDEO_HEIGHT - 200) // 2
    
    draw.text((title_x, title_y), text, fill=(255, 255, 255), font=title_font)
    
    # Draw subtitle if provided
    if subtitle:
        sub_bbox = draw.textbbox((0, 0), subtitle, font=subtitle_font)
        sub_width = sub_bbox[2] - sub_bbox[0]
        sub_x = (VIDEO_WIDTH - sub_width) // 2
        sub_y = title_y + 150
        draw.text((sub_x, sub_y), subtitle, fill=(156, 163, 175), font=subtitle_font)
    
    img.save(output_path)
    return str(output_path)

def scale_image_to_fit(input_path, output_path, width=VIDEO_WIDTH, height=VIDEO_HEIGHT):
    """Scale image to fit video dimensions, adding black bars if needed"""
    img = Image.open(input_path)
    
    # Calculate aspect ratios
    img_ratio = img.width / img.height
    target_ratio = width / height
    
    if img_ratio > target_ratio:
        # Image is wider, fit to width
        new_width = width
        new_height = int(width / img_ratio)
    else:
        # Image is taller, fit to height
        new_height = height
        new_width = int(height * img_ratio)
    
    # Resize
    img = img.resize((new_width, new_height), Image.Resampling.LANCZOS)
    
    # Create canvas with black background
    canvas = Image.new('RGB', (width, height), (0, 0, 0))
    paste_x = (width - new_width) // 2
    paste_y = (height - new_height) // 2
    canvas.paste(img, (paste_x, paste_y))
    
    canvas.save(output_path)
    return str(output_path)

