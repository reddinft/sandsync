def prepare_frames(scenes):
    """Prepare all frames for video composition"""
    prepared_frames = []
    
    for i, (source, scene_type, duration) in enumerate(scenes):
        source_path = Path(source)
        if not source_path.exists():
            print(f"⚠️  Skipping missing: {source_path}")
            continue
        
        output_path = TEMP_DIR / f"frame_{i:02d}.png"
        
        # Scale to video dimensions
        scale_image_to_fit(str(source_path), str(output_path))
        
        # Calculate frame count for this scene
        frame_count = int(duration * FPS)
        
        for _ in range(frame_count):
            prepared_frames.append(str(output_path))
    
    return prepared_frames

def create_video_from_frames(frames, output_file):
    """Create MP4 video from frame sequence using ffmpeg"""
    if not frames:
        print("❌ No frames to process!")
        return False
    
    # Write frame list to concat file
    concat_file = TEMP_DIR / "concat.txt"
    with open(concat_file, 'w') as f:
        for frame in frames:
            # Each frame gets displayed for 1 frame duration
            f.write(f"file '{frame}'\nduration {1/FPS}\n")
    
    # Use ffmpeg concat demuxer
    cmd = [
        "/Users/loki/.local/bin/ffmpeg",
        "-f", "concat",
        "-safe", "0",
        "-i", str(concat_file),
        "-c:v", "libx264",
        "-pix_fmt", "yuv420p",
        "-crf", "20",  # Quality (lower = better, 0-51)
        "-vf", f"fps={FPS}",
        "-preset", "slow",  # Slower = better compression
        "-y",  # Overwrite output
        str(output_file)
    ]
    
    print(f"🎬 Encoding video: {output_file}")
    print(f"📊 Total frames: {len(frames)}")
    
    result = subprocess.run(cmd, capture_output=True, text=True)
    
    if result.returncode != 0:
        print(f"❌ FFmpeg error: {result.stderr}")
        return False
    
    return True

